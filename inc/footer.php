<script>
$(document).ready(function() {
  // Get all elements with the class "acction-button"
  $(".acction-button a").click(function(e) {
    e.preventDefault();
    var link = this;
    // Show the confirmation modal
    $("#confirmationModal").css("display", "block");
    // When the user clicks on the confirm button, proceed with the action
    $("#confirmBtn").click(function() {
      window.location.href = link.href;
    });
  });

  // When the user clicks on <span> (x) or outside of the modal, close it
  $(".close, #confirmationModal").click(function() {
    $("#confirmationModal").css("display", "none");
  });
});

</script>